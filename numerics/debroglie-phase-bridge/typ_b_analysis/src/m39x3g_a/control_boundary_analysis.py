def scan_k3a_coupled_boundary(
    *,
    original_df: pd.DataFrame,
    base_df: pd.DataFrame,
    feature_columns: Sequence[str],
    grid_scales: Sequence[float] = (1.10, 1.20, 1.30),
    rigidity_scales: Sequence[float] = (0.90, 0.80, 0.70),
    grid_noise_pct: float = 0.10,
    rigidity_noise_pct: float = 0.10,
    n_resamples: int = 100,
    seed: int = 1729,
    distance_metric: str = "euclidean",
    rules: TypeBRuleConfig | None = None,
) -> pd.DataFrame:
    if rules is None:
        rules = _default_type_b_rules()

    rows: list[BoundaryScanRow] = []
    k = 0

    for grid_scale in grid_scales:
        for rigidity_scale in rigidity_scales:
            control_df = build_coupled_structural_disruption_control(
                base_df,
                control_family="K3a",
                grid_scale=grid_scale,
                rigidity_scale=rigidity_scale,
                grid_noise_pct=grid_noise_pct,
                rigidity_noise_pct=rigidity_noise_pct,
                seed=seed + k,
            )
            diagnostic, resampling = _evaluate_single_control_variant(
                original_df=original_df,
                control_df=control_df,
                control_family="K3a",
                feature_columns=feature_columns,
                n_resamples=n_resamples,
                seed=seed + k,
                distance_metric=distance_metric,
                rules=rules,
            )
            rows.append(
                _row_from_results(
                    control_family="K3a",
                    scan_type="coupled_boundary",
                    diagnostic=diagnostic,
                    resampling=resampling,
                    grid_scale=grid_scale,
                    rigidity_scale=rigidity_scale,
                    grid_noise_pct=grid_noise_pct,
                    rigidity_noise_pct=rigidity_noise_pct,
                    notes="Coupled structural disruption boundary scan.",
                )
            )
            k += 1

    return pd.DataFrame([asdict(r) for r in rows])


def build_full_boundary_scan(
    *,
    original_df: pd.DataFrame,
    base_df: pd.DataFrame,
    feature_columns: Sequence[str],
    n_resamples: int = 100,
    seed: int = 1729,
    distance_metric: str = "euclidean",
    rules: TypeBRuleConfig | None = None,
) -> pd.DataFrame:
    if rules is None:
        rules = _default_type_b_rules()

    df_k1 = scan_k1a_grid_boundary(
        original_df=original_df,
        base_df=base_df,
        feature_columns=feature_columns,
        n_resamples=n_resamples,
        seed=seed,
        distance_metric=distance_metric,
        rules=rules,
    )
    df_k2 = scan_k2a_rigidity_boundary(
        original_df=original_df,
        base_df=base_df,
        feature_columns=feature_columns,
        n_resamples=n_resamples,
        seed=seed + 100,
        distance_metric=distance_metric,
        rules=rules,
    )
    df_k3 = scan_k3a_coupled_boundary(
        original_df=original_df,
        base_df=base_df,
        feature_columns=feature_columns,
        n_resamples=n_resamples,
        seed=seed + 200,
        distance_metric=distance_metric,
        rules=rules,
    )

    return pd.concat([df_k1, df_k2, df_k3], axis=0, ignore_index=True)
